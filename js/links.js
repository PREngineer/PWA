function goHome()
{
	var hide = document.getElementById("offline");
	hide.style.display = 'none';
	
	var hide = document.getElementById("home");
	hide.style.display = 'block';
}

function goOffline()
{
	var hide = document.getElementById("home");
	hide.style.display = 'none';
	
	var hide = document.getElementById("offline");
	hide.style.display = 'block';
}

function load()
{
	var hide = document.getElementById("offline");
	hide.style.display = 'none';
	
	var hide = document.getElementById("home");
	hide.style.display = 'block';
}